export const useTranslation = (language) => {
    return (keyPath) => {
        const keys = keyPath.split('.');
        let result = translations[language];
        for (const key of keys) {
            if (result && result[key]) {
                result = result[key];
            } else {
                return keyPath; // Fallback to key if not found
            }
        }
        return result;
    };
};
